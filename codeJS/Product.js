class Product {
    constructor(name, price, description, sizes, colors, images, sku, categories, reviews) {
        this.name = name;
        this.price = price;
        this.description = description;
        this.sizes = sizes;
        this.colors = colors;
        this.images = images;
        this.sku = sku;
        this.categories = categories;
        this.reviews = reviews;
    }
}




